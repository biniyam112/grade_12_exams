List<int> answerProvider(Answer answerProp) {
  // key is of type answer and value is of type List<int>
  List<int> answer = [];
  answers.forEach((key, value) {
    if (key.subject == answerProp.subject && key.year == answerProp.year) {
      answer = value;
    }
  });
  return answer;
}

